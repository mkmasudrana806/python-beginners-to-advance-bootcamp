# Arithmetic Operatos are: +, -, *, /, %, **, =

#   + 
add = 10 + 20
print(add)

#   -
sub = 10 - 20
print(sub)

# *
mul = 10 * 20
print(mul)

# /
div = 10 / 20
print(div) # return quotient

# % 
a = 10
b = 3
remainder = a % b # return the remainder 
print(remainder)

# ** exponential operator
exp = 2 ** 3 # similar to:  2 * 2 * 2
print(exp)

# // floor division
a = 15
b = 2
ans = a // b
print(ans) # return floor value that is 7